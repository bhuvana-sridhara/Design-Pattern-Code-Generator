package com.bhuvana.designpatterndemo;

public class WriterHelperClass {
    public String packageName;
    public String path;

    public void setPath(String path1) {
        this.path = path1;
    }

    public String getPath() {
        return this.path;
    }

    public void setPackageName(String packageName1) {
        this.packageName = packageName1;
    }

    public String getPackageName() {
        return this.packageName;
    }
}
